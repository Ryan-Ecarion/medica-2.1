    private void switchPanel(String panelName) {
        // Pindahkan panel menggunakan CardLayout
        CardLayout cardLayout = (CardLayout) pnl_utama.getLayout();
        cardLayout.show(pnl_utama, panelName);
    }

    private void setActiveLabel(JLabel label) {
        if (activeLabel != null) {
            // Reset garis putih dari label sebelumnya
            activeLabel.setBorder(null);
        }
        // Set garis putih di bawah label yang baru
        label.setBorder(BorderFactory.createMatteBorder(0, 0, 2, 0, Color.WHITE));
        activeLabel = label;
    }
    



