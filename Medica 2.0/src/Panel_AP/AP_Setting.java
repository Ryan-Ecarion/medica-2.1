    private void btn_outMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_outMouseClicked
        // TODO add your handling code here:
      int pilihan = JOptionPane.showConfirmDialog(
            this, 
            "Apakah Anda ingin keluar?", 
            "Konfirmasi Keluar", 
            JOptionPane.YES_NO_OPTION, 
            JOptionPane.QUESTION_MESSAGE
        );

        if (pilihan == JOptionPane.YES_OPTION) {
            Window window = SwingUtilities.windowForComponent(this);
            if (window != null) {
                window.dispose(); // Menutup window yang sedang aktif
            }
            B_Login loginFrame = new B_Login();
            loginFrame.setVisible(true); // Menampilkan halaman login
        }
    }//GEN-LAST:event_btn_outMouseClicked

